About Snell Experts



Snell Experts is a United States of America owned and operated nationwide directory of environmental and property restoration specialists. Our corporate office is located in Charlotte, North Carolina. We are seen in most cities throughout the states, and have our website broken down so that people can find proven experts, even in the smallest of locations.



We are more than a digital directory; we are a team of people who have different backgrounds in the industry. We consist of anything from certified Mold inspectors; on over to people that can wire a house from scratch. You can say that being in business over 25 years comes with battle scars. We have certainly paid our dues and have learned a lot over the years. Our goal is take what we know, and find the best possible experts just to pass them on to those searching to get their environmental issues resolved. We only allow proven and highly rated talent to assist those searching for help.



To get a better idea of whom we are and how we operate, we have taken time to interview the owner of our company. If you still have more questions about us, please don’t hesitate to contact us. We will gladly answer all of your questions and resolve any concerns!



Through the eyes of the owner...



- Interviewer Question:
So who is the owner of Snell Experts?
- Owner Response:
I am. My name is David Snell.



- Interviewer Question:
Why did you create SnellExperts.com?
- Owner Response:
The main purpose was to try and get more exposure to the tried and true Mom and Pop companies out there. We are in a financial depression, regardless of what the media says.
I started noticing that many of the experts, who do a great job, were going out of business in droves.

I also wanted to either find a platform, or build one to get the really talented people in one spot for the customers that my restoration company could not assist, due to location. The goal was to be able to help others find a company like mine, so that I know they are getting the best possible care. I hate that many times my company cannot be in 20 places at one time, so SnellExperts.com fixes that.



- Interviewer Question:
What companies are allowed to be in our directory?
- Owner Response:
We allow any company to be a part of us that is involved in the environmental and restoration field, but there is a notable catch. These individuals, or companies must have at least an A rating, or higher with the Better Business Bureau (BBB), have quality reviews online, and be certified in their respected outfit (if available).

We do allow other types of “Sponsors” in our directory. These sponsors can only appear in a few places on our website, and they do not have to be in our restoration services categories.



- Interviewer Question:
How did you get started in the restoration industry?
- Owner Response:
I started out with carpet and furniture cleaning in 1987. I love cleanliness! It’s the next best thing to godliness to me. I then shifted over to Mold inspection, Mold removal, fire damage repair, crime scene cleanup, water extraction, and water damage repair. It felt natural to me that I should cleanup all of the mess, not just what’s on the carpets.



- Interviewer Question:
What kind of experience do you have personally in this field?
- Owner Response:
As stated before, I started in 1987 doing carpets, but soon shifted over to more advanced fields of cleanup. I own Executive Restoration, LLC, which is a local company to Charlotte, NC. I have had the pleasure of assisting customers through my company for a long time now, but thanks to Snell Experts, I also have the opportunity to help other specialists, and companies within my industry find new customers.



- Interviewer Question:
What do you love about your job?
- Owner Response:
It’s really neat to be on both sides of the fence. I get to work with customers to help them have a better living, or working environment, and also assist restoration companies in finding new customers. I have always loved being a problem solver and this directory, alongside Executive Restoration, lets me thrive.



- Interviewer Question:
What if a specialist or company cannot afford to join your directory?
- Owner Response:
Well, I guess I will first start with the normal rates, then go into our assistance with companies. The normal rate for us to help companies get exposure is $95 per month, per city that they are in. We have a promo at the time of this interview that if they choose 5 cities, they get each city for only $20 per month, per city. This is for a minimum of a year service from us, as we don’t do month to month service due to the amount of work that goes into helping specialists, and companies find customers.

With that said, we do offer other ways to be on the directory that requires no money at all. What we usually do for people that are struggling, is to offer a trade of service, or trade of product credit. They can give us some labor credits so that we can help customers that really need a discount, or we they can give us products, at same value. Again, most of the time these service, or product credits are given to the customers that have no way of paying a restoration company to help resolve urgent matters.

Another way of getting on here for free is to recommend other people to our directory. If you get us 5 qualified specialists, or companies that fit our criteria, we will give you exposure for free. It’s painless for those who work with people in other fields. An example would be that a tree specialist knows lots of people in landscaping, roof damage repair, and water damage repair. He can recommend his friends, and now can get our premium exposure without charge.



- Interviewer Question:
Where do you expect to see Snell Experts in the next 5 years?
- Owner Response:
We will have a global presence in 5 years. At the very least, we will be in Canada and South America within the next 5 years. I have big goals for our team, and I know we can work together to help the world.



- Interviewer Question:
Can someone be kicked out of your directory?
- Owner Response:
The simple answer is yes. We look deep into companies that provide service, during their service with us. If we hear of a complaint, we thoroughly investigate the situation, without bias. In other words, we moderate with the customer and expert, to find a resolve. If we find that an expert has been unreliable, we will remove them from the directory. They will not be refunded, and in rare cases, may be turned into the proper authorities.



- Interviewer Question:
What methods do you use for quality control on the directory?
- Owner Response:
We pre-screen anyone that wants to be on our directory. We work as a team to find out everything there is to know about the specialist, or company that is inquiring to be in our directory. I then personally give the go ahead, or denial. Again, we also do on-going screens to make sure the customers are getting the best of service.



- Interviewer Question:
Why would a company want to join your directory?
- Owner Response:
If a specialists or company has been approved, they are immediately distinct by being listed on SnellExperts.com. We showcase to thousands of visitors daily, the utmost honorable brands in the industry. You will gain instant recognition as one of the top brands in the field. You will also gain access to an entire market of potential customers.









Thank you for reading about Snell Experts! Again, if you have any further questions, or would like to apply, go to our Contact Us page. We will be glad to answer your questions.
